public class Cordinates {

    /*
    -------------------------- Fields --------------------------
     */
    private int yK;
    private int xK;


    /*
    ----------------------- Constructor -------------------------
     */
    Cordinates(int yK, int xK) {
        this.yK = yK;
        this.xK = xK;

    }
    /*
    ---------------------- Support Methods ----------------------
     */
    public int getyK() {
        return yK;
    }
    public int getxK() {
        return xK;
    }



}
